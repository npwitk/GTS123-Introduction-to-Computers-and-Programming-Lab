firstNo = int(input("Enter the first integer number: "))
secondNo = int(input("Enter the second integer number: "))

sumFS = firstNo + secondNo
diffFS = firstNo - secondNo
prodFS = firstNo * secondNo
quotFS = firstNo / secondNo

print(f"The sum is {sumFS}")
print(f"The difference is {diffFS}")
print(f"The product is {prodFS}")
print(f"The quotient is {quotFS}")